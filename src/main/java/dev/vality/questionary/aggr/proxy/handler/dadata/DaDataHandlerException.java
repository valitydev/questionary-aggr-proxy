package dev.vality.questionary.aggr.proxy.handler.dadata;

public class DaDataHandlerException extends RuntimeException {

    public DaDataHandlerException() {
        super();
    }

    public DaDataHandlerException(String message) {
        super(message);
    }

    public DaDataHandlerException(String message, Throwable cause) {
        super(message, cause);
    }

    public DaDataHandlerException(Throwable cause) {
        super(cause);
    }

}
